-- ===================== 4.11. TRUY VẤN NÂNG CAO =====================

-- Q30: Hàng hóa sắp hết hạn sử dụng (trong 30 ngày tới)

SELECT MA_HH, TEN_HH, SO_LUONG, HAN_SU_DUNG,
  DATEDIFF(DAY, GETDATE(), HAN_SU_DUNG) AS CON_LAI_NGAY
FROM HANG_HOA
WHERE DATEDIFF(DAY, GETDATE(), HAN_SU_DUNG) <= 30
  AND DATEDIFF(DAY, GETDATE(), HAN_SU_DUNG) >= 0
ORDER BY HAN_SU_DUNG;

GO

-- Q31: Bảng tổng hợp tồn kho – nhập – bán – bỏ theo từng mặt hàng

SELECT
  HH.MA_HH,
  HH.TEN_HH,
  HH.SO_LUONG AS TON_KHO,
  ISNULL(NHAP.TONG_NHAP, 0) AS DA_NHAP,
  ISNULL(BAN.TONG_BAN, 0)  AS DA_BAN,
  ISNULL(BO.TONG_BO, 0)    AS DA_BO
FROM HANG_HOA HH
LEFT JOIN (
  SELECT MA_HH, SUM(SO_LUONG) AS TONG_NHAP FROM HANG_NHAP GROUP BY MA_HH
) NHAP ON HH.MA_HH = NHAP.MA_HH
LEFT JOIN (
  SELECT MA_HH, SUM(SO_LUONG) AS TONG_BAN FROM CTHD GROUP BY MA_HH
) BAN ON HH.MA_HH = BAN.MA_HH
LEFT JOIN (
  SELECT MA_HH, SUM(SO_LUONG) AS TONG_BO FROM BO_HANG GROUP BY MA_HH
) BO ON HH.MA_HH = BO.MA_HH
ORDER BY HH.MA_HH;

GO

-- ===================== 4.12. TRUY VẤN KIỂM CHỨNG TRIGGER =====================

-- Q33: Kiểm chứng hạng khách hàng khớp với điểm tích lũy
-- (Xác minh TRG05 và TRG06 hoạt động đúng)

SELECT
  CN.TEN,
  KH.KHTT AS HANG_THE_HIEN_TAI,
  KH.DIEM,
  CASE
    WHEN KH.DIEM >= 5000 THEN N'Kim cương'
    WHEN KH.DIEM >= 1500 THEN N'Vàng'
    WHEN KH.DIEM >= 500  THEN N'Bạc'
    ELSE N'Thân thiết'
  END AS HANG_THE_TINH_TOAN,
  COUNT(HD.MA_HD) AS SO_HOA_DON_MUA,
  SUM(HD.TONG_TIEN) AS TONG_CHI_TIEU,
  FLOOR(SUM(HD.TONG_TIEN) / 100000) AS DIEM_THEO_CHI_TIEU
FROM KHACH_HANG KH
INNER JOIN CON_NGUOI CN ON KH.ID_KH = CN.ID_CN
LEFT JOIN HOA_DON HD ON KH.ID_KH = HD.ID_KH
GROUP BY CN.TEN, KH.KHTT, KH.DIEM
ORDER BY TONG_CHI_TIEU DESC;

GO

-- Q34: Kiểm chứng tồn kho: So sánh tồn kho hiện tại vs tính toán từ nhập/bán/bỏ
-- (Xác minh TRG02, TRG03, TRG04 hoạt động đúng)

SELECT
  HH.MA_HH,
  HH.TEN_HH,
  HH.SO_LUONG AS TON_KHO_HIEN_TAI,
  ISNULL(NHAP.TONG_NHAP, 0) AS TONG_NHAP,
  ISNULL(BAN.TONG_BAN, 0) AS TONG_BAN,
  ISNULL(BO.TONG_BO, 0) AS TONG_BO,
  ISNULL(NHAP.TONG_NHAP, 0) - ISNULL(BAN.TONG_BAN, 0) - ISNULL(BO.TONG_BO, 0) AS TON_KHO_TINH_TOAN,
  CASE
    WHEN HH.SO_LUONG = (ISNULL(NHAP.TONG_NHAP, 0) - ISNULL(BAN.TONG_BAN, 0) - ISNULL(BO.TONG_BO, 0))
    THEN N'✓ Khớp'
    ELSE N'✗ Sai lệch'
  END AS KIEM_CHUNG
FROM HANG_HOA HH
LEFT JOIN (
  SELECT MA_HH, SUM(SO_LUONG) AS TONG_NHAP FROM HANG_NHAP GROUP BY MA_HH
) NHAP ON HH.MA_HH = NHAP.MA_HH
LEFT JOIN (
  SELECT MA_HH, SUM(SO_LUONG) AS TONG_BAN FROM CTHD GROUP BY MA_HH
) BAN ON HH.MA_HH = BAN.MA_HH
LEFT JOIN (
  SELECT MA_HH, SUM(SO_LUONG) AS TONG_BO FROM BO_HANG GROUP BY MA_HH
) BO ON HH.MA_HH = BO.MA_HH
ORDER BY HH.MA_HH;

GO

-- Q35: Kiểm chứng số ca làm: So sánh SO_CA_LAM trong LUONG vs thực tế từ CHAM_CONG
-- (Xác minh TRG07 hoạt động đúng)

SELECT
  CN.TEN,
  CV.TEN_CV,
  L.SO_CA_LAM AS CA_LAM_TRONG_LUONG,
  COUNT(CC.MA_CA) AS CA_LAM_THUC_TE,
  CASE
    WHEN L.SO_CA_LAM = COUNT(CC.MA_CA)
    THEN N'✓ Khớp'
    ELSE N'✗ Sai lệch'
  END AS KIEM_CHUNG
FROM LUONG L
INNER JOIN CON_NGUOI CN ON L.ID_NV = CN.ID_CN
INNER JOIN CHUC_VU CV ON L.MA_CV = CV.MA_CV
LEFT JOIN CHAM_CONG CC ON L.ID_NV = CC.ID_NV
GROUP BY CN.TEN, CV.TEN_CV, L.SO_CA_LAM
ORDER BY CN.TEN;

GO

