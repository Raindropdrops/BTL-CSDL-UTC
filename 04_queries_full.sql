-- ===================== 4.1. TRUY VẤN CƠ BẢN =====================

-- Q01: Liệt kê tất cả hàng hóa có giá từ 10 triệu đến 30 triệu

SELECT MA_HH, TEN_HH, GIA, LOAI_HH
FROM HANG_HOA
WHERE GIA BETWEEN 10000000 AND 30000000
ORDER BY GIA;

GO

-- Q02: Tìm hàng hóa có tên chứa từ 'Samsung'

SELECT MA_HH, TEN_HH, GIA, SO_LUONG
FROM HANG_HOA
WHERE TEN_HH LIKE N'%Samsung%';

GO

-- Q03: Liệt kê nhân viên đang làm việc (có thông tin họ tên, chức vụ)

SELECT CN.TEN, NV.TRANG_THAI, CV.TEN_CV
FROM NHAN_VIEN NV
INNER JOIN CON_NGUOI CN ON NV.ID_NV = CN.ID_CN
INNER JOIN CHUC_VU CV ON NV.MA_CV = CV.MA_CV
WHERE NV.TRANG_THAI = N'Đang làm việc';

GO

-- Q04: Liệt kê tất cả khách hàng cùng hạng thẻ, sắp xếp theo điểm giảm dần

SELECT CN.TEN, CN.SDT, KH.KHTT, KH.DIEM
FROM KHACH_HANG KH
INNER JOIN CON_NGUOI CN ON KH.ID_KH = CN.ID_CN
ORDER BY KH.DIEM DESC;

GO

-- ===================== 4.2. JOIN NHIỀU BẢNG =====================

-- Q05: Chi tiết hóa đơn kèm tên nhân viên, tên khách hàng, ca làm

SELECT
  HD.MA_HD,
  NV_CN.TEN AS NHAN_VIEN,
  KH_CN.TEN AS KHACH_HANG,
  CL.THOI_GIAN AS CA,
  CL.NGAY,
  HD.TONG_TIEN
FROM HOA_DON HD
INNER JOIN CON_NGUOI NV_CN ON HD.ID_NV = NV_CN.ID_CN
INNER JOIN CON_NGUOI KH_CN ON HD.ID_KH = KH_CN.ID_CN
INNER JOIN CA_LAM CL ON HD.MA_CA = CL.MA_CA
ORDER BY HD.THOI_GIAN;

GO

-- Q06: Chi tiết từng dòng hóa đơn gồm tên hàng, số lượng, đơn giá, thành tiền

SELECT
  CT.MA_CTHD,
  HD.MA_HD,
  HH.TEN_HH,
  CT.SO_LUONG,
  CT.GIA AS DON_GIA,
  CT.SO_LUONG * CT.GIA AS THANH_TIEN
FROM CTHD CT
INNER JOIN HOA_DON HD ON CT.MA_HD = HD.MA_HD
INNER JOIN HANG_HOA HH ON CT.MA_HH = HH.MA_HH
ORDER BY HD.MA_HD, CT.MA_CTHD;

GO

-- Q07: Lịch chấm công chi tiết: Nhân viên – Ca – Ngày

SELECT
  CN.TEN,
  CV.TEN_CV,
  CL.THOI_GIAN AS CA,
  CL.NGAY
FROM CHAM_CONG CC
INNER JOIN NHAN_VIEN NV ON CC.ID_NV = NV.ID_NV
INNER JOIN CON_NGUOI CN ON NV.ID_NV = CN.ID_CN
INNER JOIN CHUC_VU CV ON NV.MA_CV = CV.MA_CV
INNER JOIN CA_LAM CL ON CC.MA_CA = CL.MA_CA
ORDER BY CL.NGAY, CL.THOI_GIAN, CN.TEN;

GO

-- Q08: Thông tin nhập hàng chi tiết: Hàng hóa – Nhà cung cấp – Đơn giá nhập

SELECT
  HN.MA_NHAP_HANG,
  HH.TEN_HH,
  NCC.TEN AS NHA_CUNG_CAP,
  HN.SO_LUONG,
  HN.DON_GIA,
  HN.SO_LUONG * HN.DON_GIA AS TONG_GIA_NHAP,
  HN.NGAY_NHAP
FROM HANG_NHAP HN
INNER JOIN HANG_HOA HH ON HN.MA_HH = HH.MA_HH
INNER JOIN NHA_CUNG_CAP NCC ON HN.MA_NCC = NCC.MA_NCC
ORDER BY HN.NGAY_NHAP;

GO

-- ===================== 4.3. GROUP BY & HAVING =====================

-- Q09: Doanh thu theo từng ngày

SELECT
  CAST(HD.THOI_GIAN AS DATE) AS NGAY,
  COUNT(*) AS SO_HOA_DON,
  SUM(HD.TONG_TIEN) AS DOANH_THU
FROM HOA_DON HD
GROUP BY CAST(HD.THOI_GIAN AS DATE)
ORDER BY NGAY;

GO

-- Q10: Doanh thu theo từng nhân viên bán hàng

SELECT
  CN.TEN AS NHAN_VIEN,
  COUNT(HD.MA_HD) AS SO_DON,
  SUM(HD.TONG_TIEN) AS TONG_DOANH_THU
FROM HOA_DON HD
INNER JOIN CON_NGUOI CN ON HD.ID_NV = CN.ID_CN
GROUP BY CN.TEN
ORDER BY TONG_DOANH_THU DESC;

GO

-- Q11: Số lượng hàng hóa theo từng loại

SELECT
  LOAI_HH,
  COUNT(*) AS SO_SAN_PHAM,
  SUM(SO_LUONG) AS TONG_TON_KHO,
  MIN(GIA) AS GIA_THAP_NHAT,
  MAX(GIA) AS GIA_CAO_NHAT,
  AVG(GIA) AS GIA_TRUNG_BINH
FROM HANG_HOA
GROUP BY LOAI_HH
ORDER BY TONG_TON_KHO DESC;

GO

-- Q12: Nhà cung cấp có tổng giá trị nhập hàng > 200 triệu

SELECT
  NCC.TEN,
  COUNT(HN.MA_NHAP_HANG) AS SO_DON_NHAP,
  SUM(HN.SO_LUONG * HN.DON_GIA) AS TONG_GIA_TRI_NHAP
FROM HANG_NHAP HN
INNER JOIN NHA_CUNG_CAP NCC ON HN.MA_NCC = NCC.MA_NCC
GROUP BY NCC.TEN
HAVING SUM(HN.SO_LUONG * HN.DON_GIA) > 200000000
ORDER BY TONG_GIA_TRI_NHAP DESC;

GO

-- Q13: Số ca làm thực tế của từng nhân viên (từ bảng CHAM_CONG)

SELECT
  CN.TEN,
  COUNT(CC.MA_CA) AS SO_CA_THUC_TE
FROM CHAM_CONG CC
INNER JOIN CON_NGUOI CN ON CC.ID_NV = CN.ID_CN
GROUP BY CN.TEN
ORDER BY SO_CA_THUC_TE DESC;

GO

-- Q14: Khách hàng có tổng chi tiêu > 30 triệu

SELECT
  CN.TEN AS KHACH_HANG,
  KH.KHTT AS HANG_THE,
  COUNT(HD.MA_HD) AS SO_DON,
  SUM(HD.TONG_TIEN) AS TONG_CHI_TIEU
FROM HOA_DON HD
INNER JOIN KHACH_HANG KH ON HD.ID_KH = KH.ID_KH
INNER JOIN CON_NGUOI CN ON KH.ID_KH = CN.ID_CN
GROUP BY CN.TEN, KH.KHTT
HAVING SUM(HD.TONG_TIEN) > 30000000
ORDER BY TONG_CHI_TIEU DESC;

GO

-- ===================== 4.4. SUBQUERY =====================

-- Q15: Hàng hóa có giá cao hơn giá trung bình của cùng loại

SELECT MA_HH, TEN_HH, GIA, LOAI_HH
FROM HANG_HOA HH1
WHERE GIA > (
  SELECT AVG(GIA) FROM HANG_HOA HH2
  WHERE HH2.LOAI_HH = HH1.LOAI_HH
)
ORDER BY LOAI_HH, GIA DESC;

GO

-- Q16: Nhân viên chưa lập hóa đơn nào

