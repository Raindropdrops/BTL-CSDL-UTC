-- ===================== 4.10. VIEW =====================

-- Q28: View tổng quan doanh thu theo ngày

CREATE VIEW vw_DoanhThuTheoNgay AS
SELECT
  CAST(HD.THOI_GIAN AS DATE) AS NGAY,
  COUNT(*) AS SO_HOA_DON,
  SUM(HD.TONG_TIEN) AS DOANH_THU,
  AVG(HD.TONG_TIEN) AS TRUNG_BINH_DON
