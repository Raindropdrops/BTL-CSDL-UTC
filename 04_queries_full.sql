-- ===================== 4.6. CASE WHEN =====================

-- Q21: Phân loại hàng hóa theo tầm giá

SELECT
  MA_HH,
  TEN_HH,
  GIA,
  CASE
    WHEN GIA < 2000000 THEN N'Giá rẻ'
    WHEN GIA BETWEEN 2000000 AND 15000000 THEN N'Tầm trung'
