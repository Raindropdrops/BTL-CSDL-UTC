-- Q22: Tính lương thực nhận cho từng nhân viên

SELECT
  CN.TEN,
  CV.TEN_CV,
  CV.LUONG_CO_BAN AS LUONG_MOI_CA,
  L.SO_CA_LAM,
  L.THUONG,
  (CV.LUONG_CO_BAN * L.SO_CA_LAM + L.THUONG) AS LUONG_THUC_NHAN,
  CASE
    WHEN (CV.LUONG_CO_BAN * L.SO_CA_LAM + L.THUONG) >= 15000000 THEN N'Mức cao'
    WHEN (CV.LUONG_CO_BAN * L.SO_CA_LAM + L.THUONG) >= 10000000 THEN N'Mức trung'
    ELSE N'Mức thấp'
  END AS MUC_LUONG
FROM LUONG L
INNER JOIN CON_NGUOI CN ON L.ID_NV = CN.ID_CN
INNER JOIN CHUC_VU CV ON L.MA_CV = CV.MA_CV
ORDER BY LUONG_THUC_NHAN DESC;

GO

